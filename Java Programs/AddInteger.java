public class AddTwoIntegers {

    public static void main(String[] args) {
        
        int first = 10;
        int second = 20;
        int  third = 30;

        System.out.println("Enter two numbers: " + first + " " + second+" "+third);
        

        System.out.println("The sum is: " + (first + second + third));
    }
}
